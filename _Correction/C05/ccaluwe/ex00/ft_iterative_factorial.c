int	ft_iterative_factorial(int nb)
{
	int	res;

	res = 1;
	if (nb < 0)
		return (0);
	else if (nb <= 1)
		return (1);
	while (nb > 0)
		res *= nb--;
	return (res);
}

#include <stdio.h>
int main(void)
{
	int test;
	test = ft_iterative_factorial(0);
	printf("%d", test);
}