#include <stdio.h>

char	*ft_strcat(char *dest, char *src)
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	while (dest[i] != '\0')
	{
		i++;
	}
	while (src[j] != '\0')
	{
		dest[i] = src[j];
		i++;
		j++;
	}
	dest[i] = '\0';
	return (dest);
}


int main(void)
{
	char chaine1[] = "1234";
	char chaine2[] = "5678";

	ft_strcat(chaine1, chaine2);
	printf("%s", chaine1);
	return(0);
}
